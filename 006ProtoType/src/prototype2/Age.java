package prototype2;

public class Age {
	int year=0;
	int value=0;
	
	public Age(int year,int value) {
		this.year = year;
		this.value =value;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}

	
	
}
